CREATE TABLE citation (
	id                            integer primary key GENERATED BY DEFAULT AS IDENTITY
	, fct_source_id               integer references FCT_SOURCE (ID)
	, original_id                 text
	, name                        text
	, author                      text
	, publication                 text
	, isbn                        text
	, notes                       text
);
COMMENT ON TABLE citation IS 'Table of bibliographic references. These references indicate from which publication the food composition data was taken';
COMMENT ON COLUMN citation.original_id IS 'The id of the referece as it appears in the original data';
Comment on COLUMN citation.fct_source_id IS 'The ID of the Food Composition Table where the reference appeared';

